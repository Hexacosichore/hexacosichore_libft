#include "../libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	j;
	int	n;

	i = 0;
	j = 0;
	n = 1;
	while ((str[j] >= 9 && str[j] <= 13) || (str[j] == ' '))
		j++;
	if (str[j] == '-' || str[j] == '+')
	{
		if (str[j++] == '-')
			n = n * -1;
	}
	while (str[j] >= 48 && str[j] <= 57)
		i = i * 10 + (str[j++] - 48);
	return (i * n);
}
